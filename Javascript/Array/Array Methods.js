// Array Methods

let a = [12, 45, 7, 89, 23, 56, 91, 34, 67, 10];

let b = [1, 2, 3, 4, 5];

// push
a.push(93);
console.log(a);

// pop
a.pop();
console.log(a);

// unshift
a.unshift(1);
console.log(a);

// shift
a.push();
console.log(a);

// indexOf
a.indexOf(89);
console.log(a);

// includes
a.includes(17);
console.log(a);

// concat
a.concat(b);
console.log(a);

// reverse
a.reverse();
console.log(a);

// slice
a.slice(3, 7);
console.log(a);

//splice
a.splice(2, 2, 77, 54);
console.log(a);

// sort
a.sort();
console.log(a);
